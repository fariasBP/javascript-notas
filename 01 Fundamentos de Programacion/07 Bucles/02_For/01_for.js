/* Bucle For
    Un bucle for es codigo que se repite hasta que la condición especificada del bucle for se evalúa
    como false. Normalmente el bucle for se usa para recorrer arrays.
    NOTACION:
        - for([expresion inicial], [condicion], [operador de incremento|decremento]) ---> itera hasta que la condicion sea false
*/
for (let i = 0; i <= 10; i++) {
    console.log(i);
}
for (let i = 10; i>=0; i--) {
    console.log(i);
}