/* OPERADORES ARITMETICOS
	son aquellos operadores que manipulan datos con expresiones Aritmeticas
	TEMAS:
		- + ---> adicion
		- - ---> sustraccion
		- * ---> productos
		- / ---> divisiones
*/

const numero_a = 10;
const numero_b = 6;
const numero_c = 7;

// Adicion
    console.log(numero_a + numero_b);
    
// Sustraccion
	console.log(numero_b - numero_c);

// multiplicacion
	console.log(numero_c * numero_a);

// Divisiones
	console.log(numero_a / numero_b);